          tol_residual = -1.0_dp_t  ! Residual tolerance (ignored if negative)

     real(dp_t), pointer :: &
          nodes(:), &       ! SDC nodes
          smat(:,:), &      ! SDC S matrix (node to node quadrature)
          smats(:,:,:)      ! User allocatable S matrices

  end type sdcquad

  interface build
     module procedure sdcquad_build
  end interface

  interface create
     module procedure sdcquad_create
     module procedure sdcquad_create_namelist
  end interface create

  interface destroy
     module procedure sdcquad_destroy
  end interface

  private :: sdcquadGL, sdcquadGR, sdcquadCC

contains


  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
  !
  ! Create (but do not build) SDC object.
  !
  ! If nnodes0 is supplied, the nodes are set by refining the number
  ! of nodes down to 'nnodes' from 'nnodes0'.
  !
  subroutine sdcquad_create(sdc, qtype, nnodes, nnodes0)
    integer,       intent(in ) :: qtype, nnodes
    type(sdcquad), intent(out) :: sdc
    integer,       intent(in ), optional :: nnodes0

    sdc%nnodes = nnodes
    sdc%qtype  = qtype
    sdc%iters  = 2*nnodes - 1

    if (present(nnodes0)) then
       sdc%nnodes0 = nnodes0
    else
       sdc%nnodes0 = nnodes
    end if
  end subroutine sdcquad_create


  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
  !
  ! Create (but do not build) SDC object, read options from file.
  !
  subroutine sdcquad_create_namelist(obj, unitno)
    type(sdcquad), intent(inout) :: obj
    integer,       intent(in   ) :: unitno

    integer    :: qtype, nnodes, iters
    real(dp_t) :: tol_residual

    namelist /sdc/ qtype, nnodes, iters, tol_residual

    qtype  = obj%qtype
    nnodes = obj%nnodes
    iters  = obj%iters
    tol_residual = obj%tol_residual

    rewind(unitno)
    read(unit=unitno, nml=sdc)

    obj%qtype   = qtype
    obj%nnodes  = nnodes
    obj%nnodes0 = nnodes
    obj%iters   = iters
    obj%tol_residual = tol_residual

    if (obj%iters == 0) then
       obj%iters = 2*obj%nnodes - 1
    end if

  end subroutine sdcquad_create_namelist


  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
  !
  ! Build SDC object.
  !
  subroutine sdcquad_build(sdc)
    type(sdcquad), intent(inout) :: sdc

    integer :: refine
    allocate(sdc%nodes(sdc%nnodes))
    allocate(sdc%smat(sdc%nnodes-1,sdc%nnodes))

    refine = (sdc%nnodes0 - 1) / (sdc%nnodes - 1)

    select case (sdc%qtype)
    case (SDC_GAUSS_LOBATTO)
       call sdcquadGL(sdc%nnodes0, refine, sdc%smat, sdc%nodes)

    case (SDC_GAUSS_RADAU)
       call sdcquadGR(sdc%nnodes0, refine, sdc%smat, sdc%nodes)

    case (SDC_CLENSHAW_CURTIS)
       call sdcquadCC(sdc%nnodes0, refine, sdc%smat, sdc%nodes)

    case default
       stop 'ERROR: sdcquad_build: invalid SDC quadrature type.'
    end select

    call mk_imex_smats(sdc)

  end subroutine sdcquad_build


  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
  !
  ! Make compact IMEX SDC integration matrices.
  !
  subroutine mk_imex_smats(sdc)
    type(sdcquad), intent(inout) :: sdc

    integer    :: m
    real(dp_t) :: dsdc(sdc%nnodes-1)

    allocate(sdc%smats(sdc%nnodes-1,sdc%nnodes,2))

    sdc%smats(:,:,1) = sdc%smat(:,:)
    sdc%smats(:,:,2) = sdc%smat(:,:)

    dsdc = sdc%nodes(2:sdc%nnodes) - sdc%nodes(1:sdc%nnodes-1)
    do m = 1, sdc%nnodes-1
       sdc%smats(m,m,1)   = sdc%smats(m,m,1)   - dsdc(m)
       sdc%smats(m,m+1,2) = sdc%smats(m,m+1,2) - dsdc(m)
    end do

  end subroutine mk_imex_smats


  !!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
  !
  ! Destroy SDC object.
  !
  subroutine sdcquad_destroy(sdc)
    type(sdcquad), intent(inout) :: sdc

    if (associated(sdc%nodes)) deallocate(sdc%nodes)
    if (associated(sdc%smat))  deallocate(sdc%smat)
    if (associated(sdc%smats)) deallocate(sdc%smats)
    
  end subroutine sdcquad_destroy


